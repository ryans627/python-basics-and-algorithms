# int
a = 12
print("a", a, type(a))

# float
b = 1.346
print("b", b, type(b))

# bool
c = True
print("c", c, type(c))

# str
d = "Say hello!"
print("d", d, type(d))

# list
e = [1, 3, 56, "omg", True, 1.231, "Thx all!", (1, 2, 4), {1231231, 2132}]
print("e", e, type(e))

# tuple
f = (1, 3, "123", True, 3, 3, 3)
print("f", f, type(f))

# dict
g = {1: 3, 2: 4}
print("g", g, type(g))

# set
h = {1, 2, 3, 3, 3, 3}
print("h", h, type(h))
