# 列表是有序的，也有索引值，默认从0开始
names_list = ['刘亦菲', '张曼玉', '赵丽颖', '杨幂', '刘诗诗', '江疏影']

# len()内建函数可以获取列表元素数量
print(len(names_list)) # 6

# 通过索引值获取元素值
# 语法格式: 列表[索引值]
print(names_list[3]) # '杨幂'
# 通过切片一次性获取多个值
print(names_list[:3]) # ['刘亦菲', '张曼玉', '赵丽颖']
# 反转列表: 设置步长为-1
print(names_list[::-1]) # ['江疏影', '刘诗诗', '杨幂', '赵丽颖', '张曼玉', '刘亦菲']