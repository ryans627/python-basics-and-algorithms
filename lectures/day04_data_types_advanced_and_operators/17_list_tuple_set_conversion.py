# 列表，元组，集合相互转化
list1 = [1, 2, 3, 4, 4, 2, 1, 1, 1, 34, 4, 4, 1]
# 将列表转化为元组
t1 = tuple(list1)
print(f"列表转化元组之后数据内容: {t1}, {type(t1)}")
print("-" * 100)

# 将列表快速去重，可以先转化为集合，再转回列表
# 将列表转化为集合
s1 = set(list1)
print(f"列表转化集合之后数据内容: {s1}, {type(s1)}")
list2 = list(s1)
print(f"集合转化为的列表是: {list2}, {type(list2)}")